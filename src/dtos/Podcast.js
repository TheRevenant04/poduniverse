class Podcast {
  constructor(id, title, author, description, owner, link, imageUrl, categories) {
    this.id = id;
    this.title = title;
    this.author = author;
    this.description = description;
    this.owner = owner;
    this.link = link;
    this.imageUrl = imageUrl;
    this.categories = categories;
  }
}
export default Podcast;
